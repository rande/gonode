type ValidationResult = ValidationMessage | null;

interface ValidationMessage {
    msg: string,
    keys: Object
}

export const Email = () => (value: string, values: Object): ValidationResult => {
    return rEmail.test(value) ? null :  {msg: 'invalid email', keys: {}};
};

export const Url = () => (value: string, values: Object): ValidationResult => {
    return rUrl.test(value) ? null :  {msg: 'invalid url', keys: {}};
};

export const MinLength = (min: number) => (value: string, values: Object): ValidationResult => {
    if (typeof value !== 'string') {
        return {
            msg: 'invalid string',
            keys: {}
        };
    }

    if (value.length < min) {
        return {
            msg: 'invalid length',
            keys: {
                'length': value.length.toString(10),
                'min': min
            }
        }
    }

    return null
};

export const MaxLength = (max: number) => (value: string, values: Object): ValidationResult => {
    if (typeof value !== 'string') {
        return {
            msg: 'invalid string',
            keys: {}
        }
    }

    if (value.length > max) {
        return {
            msg: 'invalid length',
            keys: {
                length: value.length.toString(10),
                max: max
            }
        }
    }

    return null
};

export const MustContains = (chars: Array<string>, atleast: number = 1) => (value: string, values: Object): ValidationResult => {
    if (typeof value !== 'string') {
        return {
            msg: 'invalid string',
            keys: {}
        }
    }

    let found: Array<string> = [];

    chars.map((char) => {
        if (value.includes(char) && !found.includes(char)) {
            found.push(char);
        }
    });

    if (found.length >= atleast) {
        return null;
    }

    return {
        msg: 'words must contains some characters',
        keys: {
            chars,
            atleast
        }
    };
};

export const NotEmpty = () => (value: any, values: Object): ValidationResult => {
    const error = {
        msg: 'value is empty',
        keys: {}
    };

    if (typeof value == 'string' && value.length == 0) {
        return error;
    }

    if (typeof value === 'object' && Object.keys(value).length === 0) {
        return error;
    }

    return null;
};
